    public void loadStudentID() {
        try {

            studentList = StudentController.getAllStudent();
            for (int i = 0; i < studentList.size(); i++) {
                Student student = (Student) studentList.get(i);

                if (((DefaultComboBoxModel) studentId_combo.getModel()).getIndexOf(student.getStudent_id()) == -1) {
                    studentId_combo.addItem(student.getStudent_id());
                }
                if (((DefaultComboBoxModel) studentId_combo_s.getModel()).getIndexOf(student.getStudent_id()) == -1) {
                    studentId_combo_s.addItem(student.getStudent_id());
                }

            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(mainFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(mainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

